class Vector():
    def __init__(self, x, y):
        self.x=x
        self.y=y
        self.left=None
        self.right=None
        self.up=None
        self.down=None
    def getWeight():
        return self.weight
    def setWeight(self, weight):
        self.weight=weight
    def setUp(self, up):
        self.up=up
    def setDown(self, down):
        self.down=down
    def setLeft(self, left):
        self.left=left
    def setRight(self, right):
        self.right=right
    def getUp(self):
        return self.up
    def getDown(self):
        return self.down
    def getLeft(self):
        return self.left
    def getRight(self):
        return self.right
